<script>
	$(document).ready(function() {
		$("#modalLoginBox").click(function() {
			$("#responseLogin").text("");
			$("#username").val("");
			$("#password").val("");
		});
		$("#btnLogin").click(function() {
			console.log("Clicked");
			$("#responseLogin").text("");
			var username = $("#username").val();
			var password = $("#password").val();
			if (username == "" || password == "") {
				$("#responseLogin").text("Please Enter Valid Information");
			} else {
				$("#btnIcon").removeClass("fa-sign-in");
				$("#btnIcon").addClass("fa-spinner fa-spin");
				$("#btnText").text("Logging in");
				$("#btnLogin").attr("disabled", true);
				$.ajax({
					url: "dashboard.php",
					type: "post",
					data: {
						"username": username,
						"password": password
					},
					success: function(response) {
						console.log(response);
						var resp = JSON.parse(response);
						if (resp.state == 1) {
							$("#btnText").text("Redirecting");
							window.location.replace(resp.path);
						} else {
							$("#btnLogin").attr("disabled", false);
							$("#responseLogin").text("Invalid Username/Password");
							$("#btnIcon").removeClass("fa-spinner fa-spin");
							$("#btnIcon").addClass("fa-sign-in");
							$("#btnText").text("Login");
						}
					},
					error: function(error) {
						console.log(error);
					}
				});
			}
		});
		$("#btnDetails").click(function() {
			console.log("Clicked");
			$("#response").text("");
			var cn = $("#cn").val();
			if (cn == "") {
				$("#response").text("Please Enter Valid Information");
			} else {
				$("#trackIcon").removeClass("fa-map-marker");
				$("#trackIcon").addClass("fa-spinner fa-spin");
				$("#trackText").text("Tracking");
				$.ajax({
					url: "dashboard.php",
					type: "post",
					data: {
						"cn": cn,
					},
					success: function(response) {
						$("#trackIcon").removeClass("fa-spinner fa-spin");
						$("#trackIcon").addClass("fa-map-marker");
						$("#trackText").text("Track Now");
						$(".before-click").hide();
						$(".after-click").show();
						console.log(response);
						$("#content").html(response);
					},
					error: function(error) {
						console.log(error);
					}
				});
			}
		});
		$("#cn").on("keyup", function() {
			$("#cn").val($("#cn").val().toUpperCase());
		});
		$("#track-order").click(function() {
			$(".after-click").hide();
			$(".before-click").show();
		});
		$(".selectCity").click(function(e) {
			var name = e.target.innerText;
			$("#cityOne").text(name);
			$(".topbar-office").removeClass("active");
			$("#" + name).addClass("active");
		});
	});
</script>
